    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
         try {
            InputStream jasperStream = getClass().getResourceAsStream("/Reportes/ReportesFactura.jasper");

            if (jasperStream == null) {
                throw new RuntimeException("No se encontr√≥ el archivo ReportesFactura.jasper en el classpath.");
        }

        JasperReport reporte = (JasperReport) JRLoader.loadObject(jasperStream);
        Connection conn = new Conexion().estableceConexion();

    
        Map<String, Object> parametros = new HashMap<>();
        parametros.put("IdFactura", Integer.parseInt(jTFCodigo.getText())); 

    
        JasperPrint print = JasperFillManager.fillReport(reporte, parametros, conn);

    
        JasperViewer viewer = new JasperViewer(print, false);
        viewer.setTitle("Factura #" + jTFCodigo.getText());
        viewer.setVisible(true);

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error al mostrar el reporte: " + e.getMessage());
    }
    }//GEN-LAST:event_btnBuscarActionPerformed

