    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        Conexion conexion = new Conexion();
        Connection con = conexion.estableceConexion();

        String usuario = txtUsuario.getText();
        String rol = txtRol.getText();
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        String clave = txtClave.getText();
        String idusuario = jTextField1.getText();

        String sql = "INSERT INTO usuarios (idusuarios, rol, usuario, nombre, apellido, password, activo) VALUES (?, ?, ?, ?, ?, ?, 1)";

        try {
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, idusuario);
            pst.setString(2, rol);
            pst.setString(3, usuario);
            pst.setString(4, nombre);
            pst.setString(5, apellido);
            pst.setString(6, clave);

            int filasAfectadas = pst.executeUpdate();

            if (filasAfectadas > 0) {
                JOptionPane.showMessageDialog(null, "Usuario agregado correctamente");
                cargarUsuarios(); // Llamar a la función para actualizar la tabla
            } else {
                JOptionPane.showMessageDialog(null, "Error al agregar usuario");
            }

            con.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al agregar usuario: " + e.toString());
        }     
        
        txtNombre.setText("");
        txtApellido.setText("");
        txtClave.setText("");
        txtUsuario.setText("");
    }//GEN-LAST:event_btnAgregarActionPerformed

