    private void jBtnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnActualizarActionPerformed
        String unidad = jTFUnidad.getText();
        String descripcion = jTFDescripcion.getText();
        
        if (unidad.isEmpty() || descripcion.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        try (Connection conn = new Conexion().estableceConexion();
             PreparedStatement pstmt = conn.prepareStatement("UPDATE UnidadesMedida SET unidad = ?, descripcion = ? WHERE idunidadesmedida = ?")) {
            
            pstmt.setString(1, unidad);
            pstmt.setString(2, descripcion);
            pstmt.setInt(3, Integer.parseInt(jTFCodigo.getText()));
            
            pstmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Unidad actualizada con éxito.");
            cargarDatosTabla(); // Recargar datos en la tabla
            jTFDescripcion.setText("");
            jTFUnidad.setText("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al actualizar unidad: " + e.getMessage());
        }
    }//GEN-LAST:event_jBtnActualizarActionPerformed

    private void jBtnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAgregarActionPerformed
        String unidad = jTFUnidad.getText();
        String descripcion = jTFDescripcion.getText();
        
        if (unidad.isEmpty() || descripcion.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        try (Connection conn = new Conexion().estableceConexion();
             PreparedStatement pstmt = conn.prepareStatement("INSERT INTO UnidadesMedida (idunidadesmedida, unidad, descripcion, activo) VALUES (?, ?, ?, ?)")) {
            
            pstmt.setInt(1, Integer.parseInt(jTFCodigo.getText()));
            pstmt.setString(2, unidad);
            pstmt.setString(3, descripcion);
            pstmt.setBoolean(4, true); // Activo por defecto
            
            pstmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Unidad agregada con éxito.");
            cargarDatosTabla(); // Recargar datos en la tabla
            generarID(); // Generar nuevo ID
            jTFDescripcion.setText("");
            jTFUnidad.setText("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al agregar unidad: " + e.getMessage());
        }
    }//GEN-LAST:event_jBtnAgregarActionPerformed

    private void jBtnDesactivarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnDesactivarActionPerformed
        int row = tablaUM.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione una unidad para desactivar.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        int id = Integer.parseInt(tablaUM.getValueAt(row, 0).toString());
        
        try (Connection conn = new Conexion().estableceConexion();
             PreparedStatement pstmt = conn.prepareStatement("UPDATE UnidadesMedida SET activo = false WHERE idunidadesmedida = ?")) {
            
            pstmt.setInt(1, id);
            pstmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Unidad desactivada con éxito.");
            cargarDatosTabla(); // Recargar datos en la tabla
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al desactivar unidad: " + e.getMessage());
        }
    }//GEN-LAST:event_jBtnDesactivarActionPerformed

    private void tablaUMMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaUMMouseClicked
        int row = tablaUM.getSelectedRow();
        if (row != -1) {
            // Obtener los valores de la fila seleccionada
            jTFCodigo.setText(tablaUM.getValueAt(row, 0).toString());
            jTFUnidad.setText(tablaUM.getValueAt(row, 1).toString());
            jTFDescripcion.setText(tablaUM.getValueAt(row, 2).toString());
        }
    }//GEN-LAST:event_tablaUMMouseClicked

