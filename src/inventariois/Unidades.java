    private void generarID() {
        try (Connection conn = new Conexion().estableceConexion();
             Statement stmt = conn.createStatement();
             ResultSet rs = stmt.executeQuery("SELECT MAX(idunidadesmedida) AS max_id FROM UnidadesMedida")) {
            
            if (rs.next()) {
                int maxId = rs.getInt("max_id");
                jTFCodigo.setText(String.valueOf(maxId + 1)); // Incrementar el ID
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al generar ID: " + e.getMessage());
        }
    }
    
    private void cargarDatosTabla() {
        DefaultTableModel model = (DefaultTableModel) tablaUM.getModel();
        model.setRowCount(0); // Limpiar la tabla

        try (Connection conn = new Conexion().estableceConexion();
             Statement stmt = conn.createStatement();
             ResultSet rs = stmt.executeQuery("SELECT * FROM UnidadesMedida")) { // Cargar todas las unidades

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("idunidadesmedida"),
                    rs.getString("unidad"),
                    rs.getString("descripcion"),
                    rs.getBoolean("activo") ? "Sí" : "No" // Convertir booleano a "Sí" o "No"
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar datos: " + e.getMessage());
        }
    }
    
